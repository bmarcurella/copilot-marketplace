#!/usr/bin/env node
/**
 * Generates demo-architect's reference files from the specialist skills' method blocks.
 *
 * Cowork skills cannot invoke each other, and companion files may not use `..` path traversal, so an
 * orchestrator skill cannot read a sibling skill's SKILL.md. The orchestrator therefore needs its own copy
 * of each phase method under references/. This script keeps that copy generated rather than hand-maintained.
 *
 * The source of truth is the block between <!-- method:start --> and <!-- method:end --> in each specialist
 * SKILL.md — the orchestration-neutral part, excluding intake and standalone-only offers.
 *
 *   node scripts/sync-cowork-references.mjs          # write the reference files
 *   node scripts/sync-cowork-references.mjs --check   # fail if they are out of date (used by CI)
 */

import { existsSync, readFileSync, writeFileSync, mkdirSync } from "node:fs";
import path from "node:path";
import process from "node:process";

const root = path.resolve(import.meta.dirname, "..");
const plugin = path.join(root, "cowork-plugins/customer-architect");
const orchestrator = path.join(plugin, "skills/demo-architect");

const PHASES = [
  { skill: "solution-architect", file: "architecture-method.md", title: "Architecture & design method" },
  { skill: "demo-planner", file: "demo-plan-method.md", title: "Demo plan method" },
  { skill: "demo-data-builder", file: "demo-data-method.md", title: "Demo data method" },
  { skill: "agent-scaffolder", file: "agent-scaffold-method.md", title: "Agent scaffold method" },
  { skill: "artifact-producer", file: "deliverables-method.md", title: "Deliverables method" },
];

const START = "<!-- method:start -->";
const END = "<!-- method:end -->";

function extractMethod(skill) {
  const src = path.join(plugin, "skills", skill, "SKILL.md");
  const text = readFileSync(src, "utf8");
  const start = text.indexOf(START);
  const end = text.indexOf(END);
  if (start === -1 || end === -1 || end < start) {
    throw new Error(`${skill}/SKILL.md: missing or malformed ${START} … ${END} markers`);
  }
  return text.slice(start + START.length, end).trim();
}

function render({ skill, title }) {
  // Demote headings one level so the reference file has a single H1.
  const body = extractMethod(skill).replace(/^## /gm, "### ");
  return `# ${title}

<!-- Generated by scripts/sync-cowork-references.mjs from skills/${skill}/SKILL.md — do not edit by hand. -->

Applied by \`demo-architect\` during an end-to-end build. The standalone skill \`${skill}\` uses the same
method.

${body}
`;
}

const check = process.argv.includes("--check");
const stale = [];

mkdirSync(path.join(orchestrator, "references"), { recursive: true });

for (const phase of PHASES) {
  const dest = path.join(orchestrator, "references", phase.file);
  const next = render(phase);
  const current = existsSync(dest) ? readFileSync(dest, "utf8") : null;
  if (current === next) continue;
  if (check) {
    stale.push(path.relative(root, dest));
  } else {
    writeFileSync(dest, next);
    console.log(`wrote ${path.relative(root, dest)}`);
  }
}

if (check && stale.length) {
  console.error("✖ demo-architect reference files are out of date:\n");
  for (const f of stale) console.error(`  - ${f}`);
  console.error("\nRun: node scripts/sync-cowork-references.mjs");
  process.exit(1);
}

console.log(check ? "✔ demo-architect references are in sync" : "✔ references generated");
